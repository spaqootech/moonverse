import { toast } from 'react-toastify';

export const SuccessNotifier = (status) => toast.success(status, {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    });

